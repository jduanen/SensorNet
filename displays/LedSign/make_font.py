# ── Font geometry ─────────────────────────────────────────────────────────────
UPM    = 700   # units per em  (7 rows × 100)
CELL_W = 600   # advance width (5px + 1px gap)
PX     = 100   # units per pixel


def bitmap_to_contours(glyph_cols):
    """
    Convert a 5-column bitmap glyph to a list of (x0,y0,x1,y1) pixel boxes.
    bit 0 = top row (y 600–700), bit 6 = bottom row (y 0–100).
    """
    boxes = []
    for col_idx, col_bits in enumerate(glyph_cols):
        for row in range(7):
            if (col_bits >> row) & 1:
                x0 = col_idx * PX
                x1 = x0 + PX
                # row 0 = top = y 600, row 6 = bottom = y 0
                y0 = (6 - row) * PX
                y1 = y0 + PX
                boxes.append((x0, y0, x1, y1))
    return boxes


def draw_glyph(pen, glyph_cols):
    """Draw all lit pixels as clockwise TrueType contours."""
    for (x0, y0, x1, y1) in bitmap_to_contours(glyph_cols):
        # Clockwise: BL → BR → TR → TL (TrueType filled = clockwise outer)
        pen.beginPath()
        pen.endPath()  # use moveTo/lineTo API instead via addComponent path

    # Use the simpler drawToPen / PointPen approach via recordingPen below.
    # We'll actually use pen.moveTo / pen.lineTo / pen.endPath via SegmentPen.
    pass


def add_glyph_contours(pen, glyph_cols):
    """Write pixel squares as closed SegmentPen contours (clockwise)."""
    for (x0, y0, x1, y1) in bitmap_to_contours(glyph_cols):
        pen.moveTo((x0, y0))   # BL
        pen.lineTo((x1, y0))   # BR
        pen.lineTo((x1, y1))   # TR
        pen.lineTo((x0, y1))   # TL
        pen.closePath()


def build_font():
    fb = FontBuilder(UPM, isTTF=True)

    fb.setupGlyphOrder([".notdef", "space"] +
                       [chr(c) for c in range(33, 127)])

    # character map: space → 32, printable ASCII 33-126
    cmap = {32: "space"}
    for c in range(33, 127):
        cmap[c] = chr(c)
    fb.setupCharacterMap(cmap)

    # metrics: (advance_width, lsb)
    metrics = {".notdef": (CELL_W, 0), "space": (CELL_W, 0)}
    for c in range(33, 127):
        metrics[chr(c)] = (CELL_W, 0)
    fb.setupHorizontalMetrics(metrics)

    # ── draw glyphs ──────────────────────────────────────────────────────────
    from fontTools.pens.ttGlyphPen import TTGlyphPen

    glyphs = {}

    # .notdef — empty box outline
    pen = TTGlyphPen(None)
    pen.moveTo((50, 0))
    pen.lineTo((550, 0))
    pen.lineTo((550, 700))
    pen.lineTo((50, 700))
    pen.closePath()
    pen.moveTo((100, 50))
    pen.lineTo((100, 650))
    pen.lineTo((500, 650))
    pen.lineTo((500, 50))
    pen.closePath()
    glyphs[".notdef"] = pen.glyph()

    # space — no contours (empty glyph)
    pen = TTGlyphPen(None)
    glyphs["space"] = pen.glyph()

    # printable ASCII 33-126
    for code in range(33, 127):
        char = chr(code)
        font_idx = code - 32  # FONT5X7 index (0 = space=32)
        cols = FONT5X7[font_idx]
        pen = TTGlyphPen(None)
        add_glyph_contours(pen, cols)
        glyphs[char] = pen.glyph()

    fb.setupGlyf(glyphs)

    # ── font metrics ─────────────────────────────────────────────────────────
    fb.setupHorizontalHeader(ascent=UPM, descent=0)
    fb.setupNameTable({
        "familyName": "LedSign5x7",
        "styleName":  "Regular",
    })
    fb.setupOS2(
        sTypoAscender=UPM,
        sTypoDescender=0,
        sTypoLineGap=0,
        usWinAscent=UPM,
        usWinDescent=0,
        fsType=0,
        achVendID="LEDS",
    )
    fb.setupPost(isFixedPitch=1)
    fb.setupHead(unitsPerEm=UPM)

    return fb.font


def main():
    out_dir = os.path.join(os.path.dirname(__file__), "fonts")
    os.makedirs(out_dir, exist_ok=True)
    out_path = os.path.join(out_dir, "LedSign5x7.ttf")

    font = build_font()
    font.save(out_path)
    print(f"Wrote {out_path}")

    # Quick sanity: count glyphs with outlines
    glyf = font["glyf"]
    with_data = sum(
        1 for name in font.getGlyphOrder()
        if glyf[name].numberOfContours > 0
    )
    print(f"  {len(font.getGlyphOrder())} glyphs total, {with_data} with outlines")


if __name__ == "__main__":
    main()
